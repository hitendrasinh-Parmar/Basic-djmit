    <p class="card-text lead "  >The department gives its students not only theoretical knowledge but also makes students aware about the ongoing scenario of the construction industry by regularly arranging site visits for the students and also the faculty members share their knowledge and experience to the students. Further the students are encouraged to enhance their capabilities and to sharpen their knowledge and presentation skills by open group discussion conducted during the lecture hours. This gives the students an opportunity to share their views about the various topics.</p>

    <h5 class=" text-center lead">Department history</h5>

    <p class="card-text lead">The Dr.Jivraj Mehta Institute of Technology, Anand (DJMIT) established the Department of Civil Engineering in 2010. The Department of Civil Engineering offers undergraduate ‘Bachelor of Engineering’ degree that covers the entire spectrum of civilengineering. It is a four year degree course affiliated to Gujarat Technological University of Ahmedabad, Gujarat with a total intake of 60 students.</p>

     
   <p class="card-text lead">Civil Engineering, one of the fastest growing engineering fields for the past few years, is a very broad discipline which addresses the relationship and interactions between infrastructure development, growing economy and facing the ever increasing challenges of environmental issues. The department focuses mainly on two major goals. The first goal is to simplify the theoretical knowledge and represent it to the students in as simple language as possible, thereby making lecture hours more fruitful and interesting for the students. The second major goal is to guide the students how to practically use the knowledge given to them in theory, by giving them field example about where this particular technology or technique can be used. Through the course content, the educational program, site visits and regular discussions with the students, the students technical and presentation skills are greatly improved. Further, this course has helped the students in their personal and professional lives too.</p>   


