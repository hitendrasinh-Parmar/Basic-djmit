    <h5 class=" text-center lead">Department history</h5>

    <p class="card-text lead">The Dr.Jivraj Mehta Institute of Technology, Anand(DJMIT) established the Department of Computer Engineering in 2010. The Department of Computer Engineering offers undergraduate ‘Bachelor of Engineering’ degree that covers the entire spectrum of computing. It is a four year degree course affiliated to Gujarat Technological University of Ahmedabad, Gujarat with a total intake of 60 students.</p>

     
   <p class="card-text lead">Computer Engineering, one of the fastest growing engineering fields for the past few years, is a very broad discipline which addresses the relationship and interactions between software, hardware and information in solving real engineering problems. The “Bachelor of Computer Engineering” degree program has a two-part objective. The first is to provide students with a strong background in mathematics, physics, and engineering science. The second objective is to provide students with the skills necessary to be effective contributors in a quality-oriented, customer-focused environment. Courses throughout the program emphasize an open-ended, design-oriented approach to solving engineering problems. Teamwork, communication skills, an interdisciplinary approach to problem solving and a research attitude are some of the skills that the students develop. The students and the faculty are involved in R & D activities as a result of which numbers of papers are published in national & international conferences and journals. Also our staffs are continuously upgrading themselves by going for higher studies and learning new technologies.</p>   


